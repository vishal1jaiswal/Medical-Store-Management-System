    private void display(ResultSet rs)
    {
        DefaultTableModel model=(DefaultTableModel)list.getModel();
        model.setRowCount(0);
        try
        {
        do
        {
            Object[] obj ={rs.getInt("BID"),rs.getString("dateandtime"),rs.getString("pname"),rs.getString("addr"),rs.getString("dname"),rs.getFloat("total"),rs.getFloat("dis"),rs.getFloat("net")}; 
            model.addRow(obj);
        }while(rs.next());
        }
        catch(SQLException e)
        {
            e.printStackTrace();
        }
    }
