    private void ShowBillButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowBillButtonActionPerformed
        boolean flag=false;
        PreparedStatement pst=null,pst2=null;
        String st="select * from billmed where BID=?";
        String st2="select * from bills where BID=?";
        ResultSet rs=null;
        ArrayList<BillMedsClass> arra=new ArrayList<BillMedsClass>();
        try
        {
            pst=conn.prepareStatement(st);
            pst.setInt(1,Integer.parseInt(bidbox.getText()));
            rs=pst.executeQuery();
            if(!rs.first())
            {
                JOptionPane.showMessageDialog(list, "No Medicines Found!");
            }
            else
            {
                BillMedsClass bmc =new BillMedsClass();
                do
                {
                    bmc=new BillMedsClass();
                    bmc.bno=rs.getString("bno");
                    bmc.exdate=rs.getString("exdate");
                    bmc.medname=rs.getString("medname").toUpperCase();
                    bmc.qty=rs.getInt("qty");
                    bmc.rate=rs.getFloat("rate");
                    bmc.amt=rs.getFloat("amt");
                    arra.add(bmc);
                }while(rs.next());
                f.medicine_list=(ArrayList<BillMedsClass>)arra.clone();
            }
            pst.close();
            pst2=conn.prepareStatement(st2);
            pst2.setInt(1, Integer.parseInt(bidbox.getText()));
            rs=null;
            rs=pst2.executeQuery();
            if(!rs.first())
            {
                JOptionPane.showMessageDialog(list, "No data found!");
                flag=false;
            }
            else
            {
                f.billID=rs.getInt("BID");
                f.pname=rs.getString("pname");
                f.addr=rs.getString("addr");
                f.dname=rs.getString("dname");
                f.total=rs.getFloat("total");
                f.dis=rs.getFloat("dis");
                f.ntotal=rs.getFloat("net");
                flag=true;
            }
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(list, e);
        }
        if(flag==true)
        {
             DisplayBill db=new DisplayBill(f);
            db.setVisible(true);
        }
    }//GEN-LAST:event_ShowBillButtonActionPerformed

