    private void updateInventory()
    {
        int pqty=0;
        Connection conn=MySQLConnect.ConnectDB();
        PreparedStatement pst=null, pst2=null;
        String st="update medicines set qty=? where medname=?";
        String st2="select qty from medicines where medname=?";
        ResultSet rs=null;
        try
        {
            for(int i=0; i<arr.size(); i++)
            {
                pst2=conn.prepareStatement(st2);
                pst2.setString(1, arr.get(i).medname.toLowerCase());
                rs=pst2.executeQuery();
                if(!rs.first())
                {
                    JOptionPane.showMessageDialog(this, "Error");
                }
                else
                {
                    pqty=rs.getInt("qty");
                }
                
                pqty=pqty-arr.get(i).qty;
                pst=conn.prepareStatement(st);
                pst.setInt(1, pqty);
                pst.setString(2, arr.get(i).medname.toLowerCase());
                pst.execute();
                pst.close();    
            }
            conn.close();
            pst.close();
        }
        catch(SQLException e)
        {
            //JOptionPane.showMessageDialog(this, "Error creating billmeds!");
            e.printStackTrace();
        }
    }
    
    private void createbillmeds(int BID)
    {
        Connection conn=MySQLConnect.ConnectDB();
        PreparedStatement pst=null;
        String st="insert into billmed(BID, medname, qty, rate, exdate, bno, amt) values (?,?,?,?,?,?,?)";
        try
        {
            for(int i=0; i<arr.size(); i++)
            {
                pst=conn.prepareStatement(st);
                pst.setInt(1, BID);
                pst.setString(2, arr.get(i).medname);
                pst.setInt(3, arr.get(i).qty);
                pst.setFloat(4, arr.get(i).rate);
                pst.setString(5, arr.get(i).exdate);
                pst.setString(6, arr.get(i).bno);
                pst.setFloat(7, arr.get(i).amt);
                pst.execute();
                pst.close();    
            }
            conn.close();
            pst.close();
        }
        catch(SQLException e)
        {
            //JOptionPane.showMessageDialog(this, "Error creating billmeds!");
            e.printStackTrace();
        }
        
    }
            
    
    
    private int createbill()
    {
        Connection conn=MySQLConnect.ConnectDB();
        PreparedStatement pst=null, pst2=null;
        String st="insert into bills(pname, addr, dname, total, dis, net) values (?,?,?,?,?,?)";
        try
        {
            pst=conn.prepareStatement(st);
            pst.setString(1, pnameBox.getText());
            pst.setString(2, addressBox.getText());
            pst.setString(3, docNameBox.getText());
            pst.setFloat(4, total);
            pst.setFloat(5, Float.parseFloat(discountBox.getText()));
            pst.setFloat(6, Float.parseFloat(netTotalBox.getText()));
            pst.execute();
            //pst.close();
            String st2="select BID from bills where pname=? and addr=? and total=?";
            pst2=conn.prepareStatement(st2);
            pst2.setString(1, pnameBox.getText());
            pst2.setString(2, addressBox.getText());
            pst2.setFloat(3, total);
            ResultSet rs=null;
            rs=pst2.executeQuery();
            rs.first();
            //conn.close();
            //pst2.close();
            int currentID=rs.getInt("BID");
            System.out.println("BID = "+currentID);
            //rs.close();
            
            return currentID;
        }
        catch(SQLException e)
        {
            //JOptionPane.showMessageDialog(this, e);
            e.printStackTrace();
        }
        return -1;
    }

