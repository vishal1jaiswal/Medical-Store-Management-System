    private void finishButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishButtonActionPerformed
        int ID=createbill();
        if(ID!=-1)
        {
            createbillmeds(ID);
            updateInventory();
            JOptionPane.showMessageDialog(this, "Bill Successfully Created!");
            FinalBillElement f=new FinalBillElement();
            f.medicine_list=(ArrayList<BillMedsClass>)arr.clone();
            f.addr=addressBox.getText();
            f.dis=Float.parseFloat(discountBox.getText());
            f.dname=docNameBox.getText();
            f.ntotal=Float.parseFloat(netTotalBox.getText());
            f.pname=pnameBox.getText();
            f.total=Float.parseFloat(totalBox.getText());
            f.billID=ID;
            BillClass bc=new BillClass();
            bc.setVisible(true);
            DisplayBill db=new DisplayBill(f);
            db.setVisible(true);
            dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Database Error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_finishButtonActionPerformed

